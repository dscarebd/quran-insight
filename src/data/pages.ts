// Helper function to get page by number
export const getPageByNumber = (pageNumber: number): QuranPage | undefined => {
  return quranPages.find(p => p.pageNumber === pageNumber);
};

// Helper function to find page by surah and verse
export const findPageBySurahVerse = (surahNumber: number, verseNumber: number): number => {
  for (const page of quranPages) {
    if (
      (surahNumber > page.startSurah || (surahNumber === page.startSurah && verseNumber >= page.startVerse)) &&
      (surahNumber < page.endSurah || (surahNumber === page.endSurah && verseNumber <= page.endVerse))
    ) {
      return page.pageNumber;
    }
  }
  return 1;
};

